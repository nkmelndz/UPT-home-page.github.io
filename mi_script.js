$(document).ready(function () {
  $("#postulantes").hover(
    function () {
      $(".postulantes").addClass("showpostulantes");
    },
    function () {
      $(".postulantes").removeClass("showpostulantes");
    }
  );
  $(".postulantes").hover(
    function () {
      $(".postulantes").addClass("showpostulantes");
    },
    function () {
      $(".postulantes").removeClass("showpostulantes");
    }
  );
});

